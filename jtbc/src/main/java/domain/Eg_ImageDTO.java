package domain;

public class Eg_ImageDTO {

	private String c_name;
	private String tag;
	private String url;
	private String egm_id;

	public String getEgm_id() {
		return egm_id;
	}

	public void setEgm_id(String egm_id) {
		this.egm_id = egm_id;
	}

	public String getC_name() {
		return c_name;
	}

	public void setC_name(String c_name) {
		this.c_name = c_name;
	}

	public String getTag() {
		return tag;
	}

	public void setTag(String tag) {
		this.tag = tag;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

}
